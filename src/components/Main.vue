<script>
export default {
  name: 'Main',
  validators: {
    "form.name": function (value) {
      return Validator.value(value).required("Obrigatório");
    },
    "form.email": function (value) {
      return Validator.value(value).email("Email inválido");
    },
    "form.phone": function (value) {
      return Validator.value(value).required("Obrigatório");
    }
  },
  data() {
    return {
      loading: false,
      form_send: false,
      form_send_error: false,
      form: {
        name: "",
        email: "",
        phone: "",
        type_contact: [],
        interest: ""
      },
      images: {
        img_01: require('../assets/img/portfolio/fullsize/lazer-1.jpeg'),
