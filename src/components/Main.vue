        img_05: require('../assets/img/portfolio/fullsize/lazer-5.jpeg'),
        img_06: require('../assets/img/portfolio/fullsize/lazer-6.jpeg'),
      }
    }
  },
  methods: {
    submitForm() {
      const self = this;
      this.$validate().then(function (success) {
        if (success) {
          self.sendContactForm()
        }
      });
    },
    sendContactForm() {
      this.loading = true
      this.form_send_error = false

      http.get("send-form-data", { params: this.form })
        .then(result => {


          setTimeout(() => {
            this.loading = false
            this.form_send = true
          })


        }).catch(err => {
          this.loading = false
          this.form_send = false
          this.form_send_error = true
        })
    },
    cleanForm() {
      this.form = {
        name: "",
        email: "",
        phone: "",
        type_contact: [],
        interest: ""
      }
    }
  },
  mounted() {

    setTimeout(() => {

      $('#frameModalBottom').modal({
        keyboard: false,
        backdrop: 'static'
      })

    }, 4000)
     
  },
}
